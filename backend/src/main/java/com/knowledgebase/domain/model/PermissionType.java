package com.knowledgebase.domain.model;

/**
 * Тип права доступа к пространству (второй уровень RBAC).
 *
 * Права используются совместно с GlobalRole:
 * - ADMIN: игнорирует SpacePermission, всегда имеет доступ
 * - EDITOR: требует WRITE или OWNER для редактирования
 * - READER: требует READ, WRITE или OWNER для чтения
 *
 * Иерархия прав (от меньшего к большему):
 * READ < WRITE < OWNER
 */
public enum PermissionType {

    /**
     * Право на чтение документов в пространстве.
     * Доступно всем ролям для просмотра.
     */
    READ,

    /**
     * Право на создание и редактирование документов в пространстве.
     * Включает READ.
     * Доступно только для Editor и выше.
     */
    WRITE,

    /**
     * Полный контроль над пространством.
     * Включает READ и WRITE.
     * Может управлять правами других пользователей.
     */
    OWNER;

    /**
     * Проверяет, включает ли данное право право на запись.
     * WRITE и OWNER позволяют создавать/редактировать документы.
     */
    public boolean canWrite() {
        return this == WRITE || this == OWNER;
    }

    /**
     * Проверяет, включает ли данное право право на чтение.
     * Все типы прав включают чтение.
     */
    public boolean canRead() {
        return true;
    }
}
